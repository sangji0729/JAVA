package may18;

public class Hello {//클래스(대문자)
	
	
	public static void main(String[] args) {//메소드() 모든 메소드는 클래스 안에 있어야함
		//변수 선언문장;
		int number = 10; //명령문장;
		System.out.print("Hello");//명령문장;	
		//불러오기 = 호출 = 메소드이름을 적어주면 됨.
		print(); //명령문장;
	}
	
	public static void print() {
		System.out.println("프린트 문장입니다.");
	}
}

